public class SwitchProgram
{
    public static void main(String args[])
     {

           int choice = 1;
           switch(choice)
            {
                 case 1:
                        System.out.println("Case 1");
                        break;
                 case 2:
                        System.out.println("Case 2");
                        break;
                 default:
                        System.out.println("default");
                        break;
              }
      }
}
                      




